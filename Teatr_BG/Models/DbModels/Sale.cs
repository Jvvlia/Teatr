
using System;
using System.Collections.Generic;
using System.Linq;
using System.ComponentModel.DataAnnotations;
using System.Web;

namespace Teatr_BG.Models.DbModels
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A sale. </summary>
    ///
    /// <remarks>   Julia, 24.05.2023. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Sale //podsumowanie 
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the sale. </summary>
        ///
        /// <value> The identifier of the sale. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Key]
        public int SaleID { get; set; }
        //[Range(0, int.MaxValue, ErrorMessage = "Liczba biletów musi być większa lub równa zero.")]

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of tickets. </summary>
        ///
        /// <value> The total number of tickets. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int NumberTickets { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the client. </summary>
        ///
        /// <value> The identifier of the client. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int ClientID { get; set; } // klucz obcy do tabeli Clients

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the client. </summary>
        ///
        /// <value> The client. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual Client Client { get; set; } // właściwość nawigacyjna

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the play. </summary>
        ///
        /// <value> The identifier of the play. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int PlayID { get; set; } // klucz obcy do tabeli Places

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the play. </summary>
        ///
        /// <value> The play. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual Play Play { get; set; } // właściwość nawigacyjna
    }
}
